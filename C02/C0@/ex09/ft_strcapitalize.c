char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + 32;
		i++;
	}
	i = 0;
	while (str[i] != '\0')
	{
		if (i == 0 && str[i] >= 'a' && str[i] <= 'z')
			str[i] = str[i] - 32;
		if (str[i] >= 'a' && str[i] <= 'z'
				&& ((str[i - 1] >= ' ' && str[i - 1] <= 64) ||
				(str[i - 1] >= 91 && str[i - 1] <= 96) ||
				(str[i - 1] >= 123 && str[i - 1] <= 126)))
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}
