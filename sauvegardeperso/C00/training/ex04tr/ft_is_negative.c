#include <unistd.h>

void	ft_is_negative(int n)
{
	char b;
	char c;

	b = 'P';
	c = 'N';
	if (n < 0)
	{
		write(1, &c, 1);
	}
	else
	{
		write(1, &b, 1);
	}
}
