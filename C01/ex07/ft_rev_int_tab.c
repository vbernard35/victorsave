#include <stdio.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int l;

	i = 0;
	while (i < size / 2)
	{
		l = tab[size - 1 - i];
		tab[size - i - 1] = tab[i];
		tab[i] = l;
		i++;
	}
}
